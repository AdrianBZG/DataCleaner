package org.datacleaner.monitor.server.crates;

import java.util.HashMap;
import java.util.Map;

/**
 * This class contains configuration and state of a particular component.
 * @author k.houzvicka
 * @since 9. 7. 2015
 */
public class ComponentConfiguration {
    public static enum ComponentType {
        NOT_SET,
        ANALYZER,
        TRANSFORMER;
    };

    private Map<String, String> propertiesMap = new HashMap<>();
    private ComponentStatus status = ComponentStatus.CREATED;
    private Integer id = 0;
    private String componentName = "";
    private ComponentType componentType = ComponentType.NOT_SET;

    public Map<String, String> getPropertiesMap() {
        return propertiesMap;
    }

    public void setPropertiesMap(Map<String, String> propertiesMap) {
        this.propertiesMap = propertiesMap;
    }

    public ComponentStatus getStatus() {
        return status;
    }

    public void setStatus(ComponentStatus status) {
        this.status = status;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getComponentName() {
        return componentName;
    }

    public void setComponentName(String componentName) {
        this.componentName = componentName;
    }

    public ComponentType getComponentType() {
        return componentType;
    }

    public void setComponentType(ComponentType componentType) {
        this.componentType = componentType;
    }
}
